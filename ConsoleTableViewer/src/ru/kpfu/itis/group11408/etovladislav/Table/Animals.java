package ru.kpfu.itis.group11408.etovladislav.Table;

public class Animals {
	private String name;
	private int age;
	private int weight;

	public Animals(String name, int age, int weight) {
		this.name = name;
		this.age = age;
		this.weight = weight;
	}

	public String getName() {
		return name;
	}

	public int getAge() {
		return age;
	}

	public int getWeight() {
		return weight;
	}

}
